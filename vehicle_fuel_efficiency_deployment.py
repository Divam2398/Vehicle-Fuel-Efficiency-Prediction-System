import streamlit as st
import joblib
import numpy as np

model = joblib.load("fuel_efficiency_model.pkl")

st.title("Vehicle Fuel Efficiency Prediction")

cylinders = st.number_input("Cylinders")
displacement = st.number_input("Displacement")
horsepower = st.number_input("Horsepower")
weight = st.number_input("Weight")
acceleration = st.number_input("Acceleration")
model_year = st.number_input("Model Year")
origin = st.number_input("Origin")

if st.button("Predict"):
    input_data = np.array([[cylinders, displacement, horsepower, weight,
                            acceleration, model_year, origin]])

    prediction = model.predict(input_data)

    st.success(f"Predicted MPG: {prediction[0]:.2f}")
